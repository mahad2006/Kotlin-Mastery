package `04_Operators`

// Relational Operators in Kotlin
fun main() {
    val a = 10
    val b = 5

    // Relational operations
    println("Is a equal to b? ${a == b}") // Equality check
    println("Is a not equal to b? ${a != b}") // Inequality check
    println("Is a greater than b? ${a > b}") // Greater than
    println("Is a less than b? ${a < b}") // Less than
    println("Is a greater or equal to b? ${a >= b}") // Greater or equal to
    println("Is a less or equal to b? ${a <= b}") // Less or equal to
}
