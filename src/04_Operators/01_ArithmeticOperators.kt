package `04_Operators`

// Arithmetic Operators in Kotlin
fun main() {
    val a = 10
    val b = 5

    // Basic arithmetic operations
    println("Addition: ${a + b}")      // Adds a and b
    println("Subtraction: ${a - b}")   // Subtracts b from a
    println("Multiplication: ${a * b}") // Multiplies a and b
    println("Division: ${a / b}")      // Divides a by b
    println("Modulus: ${a % b}")       // Gives remainder when a is divided by b
}
